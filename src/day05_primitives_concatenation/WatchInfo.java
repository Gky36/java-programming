package day05_primitives_concatenation;

public class WatchInfo
{
    public static void main(String[] args)
    {
        String brand="Rolex";
        String color="Gold";
        double price =25.500;
        boolean waterResistant= true;
        boolean isSmart= false;
        char gender= 'M';

        System.out.println("Brand:\t\t\t\t"+brand);
        System.out.println("Color:\t\t\t\t"+color);
        System.out.println("Price:\t\t\t\t$"+price);
        System.out.println("WaterResistant\t\t"+waterResistant);
        System.out.println("Is Smart\t\t\t"+isSmart);
        System.out.println("Gender\t\t\t\t"+gender);


    }
}
