package day03_escape_sequence;

public class EscapeSequence
{
    public static void main(String[] args)
    {
        System.out.println("I like \"JAVA\"");
        System.out.println();
        System.out.println("Lets buy \"Cofee\" and \"Tea\"");
        // Eger \\ koyarsak sadece bir tane verir
        // \n koyarsak newline
        // \t tab yapar

        System.out.println("java \nintellij");
        System.out.println("\tjava\tselenium\tc++");

    }
}
