package day03_escape_sequence;

public class EscapeSequence2
{
    public static void main(String[] args)
    {
        System.out.println("\"Iphone case\",\n \"Xbox\", \"Webcam\", \"Monitor\"");
        System.out.println("   ^   ");
        System.out.println(" /   \\");
        System.out.println("|     |");
        System.out.println("--------");
    }
}

