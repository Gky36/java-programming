package day34_void_methods;

public class StartCar_03
{
    public static void main(String[] args)
    {
        seatInCar();
        startTheCar();
        shiftToDrive();
        pressGasPedal();

    }

   public static void seatInCar()
   {
       System.out.println("Open the door and seat in driver seat");
   }
   public static void startTheCar()
   {
       System.out.println("Insert key to ignition and turn clockwise");
   }
   public static void shiftToDrive()
   {
       System.out.println("Press brake pedal and shift to 'D' ");
   }
   public static void pressGasPedal()
   {
       System.out.println("Time to goes fast");
   }
}





